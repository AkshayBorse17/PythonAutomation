# Find the intersection and union of two sets.


set1 = {1, 2, 3, 4, 5}

set2 = {3, 4, 5, 6, 7}

print("Union : ",set1.union(set2))

print("Intersection : ",set1.intersection(set2))

print("Difference set1 : ",set1.difference(set2))

print("Difference set2 : ",set2.difference(set1))

print("Symmetric_difference : ",set1.symmetric_difference(set2))

